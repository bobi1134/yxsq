package org.yxsq.dto;

public class Bb {

	private int id;
	private String bb_content;
	private String bb_user;
	private String bb_time;
	private String bb_sqId;
	
	/** setter and getter method */
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getBb_content() {
		return bb_content;
	}
	public void setBb_content(String bb_content) {
		this.bb_content = bb_content;
	}
	public String getBb_user() {
		return bb_user;
	}
	public void setBb_user(String bb_user) {
		this.bb_user = bb_user;
	}
	public String getBb_time() {
		return bb_time;
	}
	public void setBb_time(String bb_time) {
		this.bb_time = bb_time;
	}
	public String getBb_sqId() {
		return bb_sqId;
	}
	public void setBb_sqId(String bb_sqId) {
		this.bb_sqId = bb_sqId;
	}
}
