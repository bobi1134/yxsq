package org.yxsq.dto;

public class Sq {

	private String id;
	private String sq_name;
	private String sq_introduction;
	private String sq_logo_img;
	private String sq_img;
	private String sq_location;
	private String sq_pano;
	private String sq_yz;
	
	/** setter and getter method */
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getSq_name() {
		return sq_name;
	}
	public void setSq_name(String sq_name) {
		this.sq_name = sq_name;
	}
	public String getSq_introduction() {
		return sq_introduction;
	}
	public void setSq_introduction(String sq_introduction) {
		this.sq_introduction = sq_introduction;
	}
	public String getSq_img() {
		return sq_img;
	}
	public void setSq_img(String sq_img) {
		this.sq_img = sq_img;
	}
	public String getSq_location() {
		return sq_location;
	}
	public void setSq_location(String sq_location) {
		this.sq_location = sq_location;
	}
	public String getSq_logo_img() {
		return sq_logo_img;
	}
	public void setSq_logo_img(String sq_logo_img) {
		this.sq_logo_img = sq_logo_img;
	}
	public String getSq_pano() {
		return sq_pano;
	}
	public void setSq_pano(String sq_pano) {
		this.sq_pano = sq_pano;
	}
	public String getSq_yz() {
		return sq_yz;
	}
	public void setSq_yz(String sq_yz) {
		this.sq_yz = sq_yz;
	}
}
